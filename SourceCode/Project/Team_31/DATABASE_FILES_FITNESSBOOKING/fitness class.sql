create database fitnessclass;
use fitnessclass;

CREATE TABLE Membership (
    MembershipID INT PRIMARY KEY,
    MembershipType VARCHAR(50),
    StartDate DATE,
    EndDate DATE
);

CREATE TABLE Instructor (
    InstructorID INT PRIMARY KEY,
    Name VARCHAR(100),
    Email VARCHAR(100),
    Phone VARCHAR(15),
    Bio TEXT
);

CREATE TABLE Location (
    LocationID INT PRIMARY KEY,
    Address VARCHAR(255)
);

CREATE TABLE Schedule (
    ScheduleID INT PRIMARY KEY,
    DayOfWeek VARCHAR(20),
    StartTime TIME,
    EndTime TIME
);

CREATE TABLE Class (
    ClassID INT PRIMARY KEY,
    ClassName VARCHAR(100),
    Description VARCHAR(255),
    ScheduleID INT,
    LocationID INT,
    InstructorID INT
);

CREATE TABLE User (
    UserID INT PRIMARY KEY,
    Name VARCHAR(100),
    Email VARCHAR(100),
    Address VARCHAR(255),
    PhoneNo VARCHAR(15),
    MembershipID INT
);

CREATE TABLE Booking (
    BookingID INT PRIMARY KEY,
    UserID INT,
    ClassID INT,
    BookingDate DATE,
    Status VARCHAR(50)
);

CREATE TABLE Payment (
    PaymentID INT PRIMARY KEY,
    UserID INT,
    Amount DECIMAL(10, 2),
    PaymentDate DATE
);

CREATE TABLE Attendance (
    AttendanceID INT PRIMARY KEY,
    BookingID INT,
    AttendanceDate DATE,
    Status VARCHAR(50)
);

CREATE TABLE Review (
    ReviewID INT PRIMARY KEY,
    UserID INT,
    ClassID INT,
    Rating INT,
    Comment TEXT,
    ReviewDate DATE
);


ALTER TABLE Class
ADD FOREIGN KEY (ScheduleID) REFERENCES Schedule(ScheduleID),
ADD FOREIGN KEY (LocationID) REFERENCES Location(LocationID),
ADD FOREIGN KEY (InstructorID) REFERENCES Instructor(InstructorID);



ALTER TABLE User
ADD FOREIGN KEY (MembershipID) REFERENCES Membership(MembershipID);

ALTER TABLE Booking
ADD FOREIGN KEY (UserID) REFERENCES User(UserID),
ADD FOREIGN KEY (ClassID) REFERENCES Class(ClassID);

ALTER TABLE Payment
ADD FOREIGN KEY (UserID) REFERENCES User(UserID);






ALTER TABLE Attendance
ADD FOREIGN KEY (BookingID) REFERENCES Booking(BookingID);

ALTER TABLE Review
ADD FOREIGN KEY (UserID) REFERENCES User(UserID),
ADD FOREIGN KEY (ClassID) REFERENCES Class(ClassID);


